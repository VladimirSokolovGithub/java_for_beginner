package Lessons.Les_15_whileAndDoWhile;

public class Test6 {
    public static void main(String[] args) {
        int i = 1;
        do {
            System.out.println(i);
            i++;
        } while (i <= 10);

        int a = 1;
        do {
            System.out.println(a);
            a++;
        } while (a == 10);

    }
}
