package Lessons.Les_21_garbageCollector;

public class TernaryOperator {
    // boolean Expression ? (если true)expression1 : (если false)expression2
    //expression1 и expression2 обязательно должны быть выражения которые возвращают какие то значения
    //но не обязательно что эти выражения должны возвращать один тип данных

    public static void main(String[] args) {
        int a = 4;
        System.out.println(a < 4 ? 7 : "privet");
    }



}
